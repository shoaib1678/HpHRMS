package com.hp.model;

public class RemainLeave {

	private String leave_type;
	private float total_leaves;
	private float remain_leaves;
	public String getLeave_type() {
		return leave_type;
	}
	public void setLeave_type(String leave_type) {
		this.leave_type = leave_type;
	}
	public float getTotal_leaves() {
		return total_leaves;
	}
	public void setTotal_leaves(float total_leaves) {
		this.total_leaves = total_leaves;
	}
	public float getRemain_leaves() {
		return remain_leaves;
	}
	public void setRemain_leaves(float remain_leaves) {
		this.remain_leaves = remain_leaves;
	}
}
